# prints true
print(4 > 3)
# prints false
print(1 < 0)

# prints true
print(7 >= 2)
print(7 >= 7) # boundary value
# prints false
print(1000 <= 250)

# prints true
print(7 != 2)
# prints false
print(7 != 7)
print(10 != 10.0) # it doesn't make difference if float or int

# prints true
print(7 == 7)
print(10 == 10.0) # it doesn't make difference if float or int
# prints false
print(10 == 100)

# prints true
print("Hello" == "Hello")
# prints false
print("Hello" == "World")
print("Hello" == "hello") # case sensitive
