# using of line continuation character "\" in math expression

ex_int = 2 + \
       4 + \
       1

print(ex_int)  # prints 7

# using of line continuation character "\"
ex_string = "The Emp\
ire Strikes \
Back"

print(ex_string)  # prints "The Empire Strikes Back" in string

ex_string_plus = "hello " + \
                 "world"

print(ex_string_plus)  # prints "hello world"
