sliced = [1, 2, 3, 4, 5, 6, 7, 8, 9]
print(sliced[:4])  # prints until 4 - it ends in the index number which is one less than typed number in brackets - 3
# prints [1, 2, 3, 4]
print(sliced[3:8])  # it starts on fourth index = 4 and ends in the index number which is one less than 8
# prints [4, 5, 6, 7, 8]
print(sliced[6:])  # it starts at the seventh index = 7 and prints all remaining numbers in the list
# prints [7, 8, 9]
