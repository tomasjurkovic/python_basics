print("".join(["Tomas, ", "this ", "is ", "how ", "strings ", "can ", "join."]))
# prints Tomas, this is how strings can join.

print(''.join(["one", "word", "only"]))
# prints onewordonly

print(",".join(["comma", "separated", "values"]))
# prints comma,separated,values

print(" ".join(["there", "are", "empty", "spaces", "between", "words"]))
# prints there are empty spaces between words

print("whatever".join(["that", "I", "can", "use"]))
# prints thatwhateverIwhatevercanwhateveruse
